using System;

namespace VariousTypes
{
    public class Program
    {
        private static void Main(string[] args)
        {
            int i = 8;
            uint j = 3U;
            long k = 16L;
            ulong n = 20UL;

            Console.WriteLine("Type Int: " + i);
            Console.WriteLine("Type UInt: " + j);
            Console.WriteLine("Type Long: " + k);
            Console.WriteLine("Type ULong: " + n);
        }
    }
}
